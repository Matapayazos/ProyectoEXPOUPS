package Entidades;

public class Bienestar {

  public String becas;

  public String quintiles;

  public String fichasocioe;

  public String descripcion;

    public Bienestar() {
    }
 
    public String getBecas() {
        return becas;
    }

    public void setBecas(String becas) {
        this.becas = becas;
    }

    public String getQuintiles() {
        return quintiles;
    }

    public void setQuintiles(String quintiles) {
        this.quintiles = quintiles;
    }

    public String getFichasocioe() {
        return fichasocioe;
    }

    public void setFichasocioe(String fichasocioe) {
        this.fichasocioe = fichasocioe;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }



}